def starting_page(request: HttpRequest) -> HttpResponse:
    sorted_posts = sorted(_posts, key=lambda post: post["date"])
    latest_posts = sorted_posts[-3:]
    return render(request, "blog/index.html", {"posts": latest_posts})


def posts(request: HttpRequest) -> HttpResponse:
    return render(request, "blog/all-posts.html", {"all_posts": _posts})


def post_detail(request: HttpRequest, slug: str) -> HttpResponse:
    identified_post = next(post for post in _posts if post["slug"] == slug)
    return render(request, "blog/post-detail.html", {"post": identified_post})
